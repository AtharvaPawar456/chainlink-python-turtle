import turtle
turtle.bgcolor("black")
t=turtle.Turtle()
t.pensize(4)
turtle.speed(5)
t.color("black")
t.begin_fill()
t.fillcolor("blue")
t.penup()
t.rt(90)
t.fd(140)
t.pendown()
t.goto(140,-50)
t.rt(180)
t.fd(100)
t.goto(0,140)
t.goto(-140,50)
t.bk(100)
t.goto(0,-140)
t.end_fill()###
t.penup()
t.fd(40)
t.pendown()
t.begin_fill()
t.fillcolor("black")
t.goto(100,-38)
t.fd(80)
t.goto(0,100)
t.goto(-100,38)
t.bk(80)
t.goto(0,-100)
t.end_fill()
t.penup()
t.bk(80)
t.pendown()
t.color("yellow")
t.write("ChainLink",'false','center',font=('arabic',20))
t.rt(90)
t.bk(120)
t.penup()
t.fd(1000)
turtle.done()